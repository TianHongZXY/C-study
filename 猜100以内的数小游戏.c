#include <stdio.h>
#include <stdlib.h>
#include <time.h>
int main()
{srand(time(0));
int number=rand()%100+1;
int count=0;
int a;
printf("我已经想好了一个1到100之间的整数。");
do{
	printf("请猜这个数是：");
	scanf("%d",&a);
	count++;
	if(a>number){
		printf("你猜的数太大了。");
	} else if(a<number){
		printf("你猜的数太小了。");
		 
	}
} while(a!=number);
printf("恭喜你，你用了%d次就猜到了答案。\n",count);
system("pause");
return 0; 
}
